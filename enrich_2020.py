def enrich_genres():
    if not os.path.exists(file_path):
        print(f"Error: File not found at {file_path}")
        return

    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated_count = 0
    unknown_artists = set()

    for entry in data:
        artist = entry.get('artist_name')
        if artist in genre_map:
            entry['artist_genre'] = genre_map[artist]
            updated_count += 1
        else:
            if entry.get('artist_genre') == 'Desconhecido' or not entry.get('artist_genre'):
                unknown_artists.add(artist)

    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)

    print(f"Successfully updated {updated_count} entries in {file_path}")
    if unknown_artists:
        print(f"\nArtists without genre mapping ({len(unknown_artists)}):")
        for artist in sorted(list(unknown_artists)):
            print(f"- {artist}")

if __name__ == "__main__":
    enrich_genres()
